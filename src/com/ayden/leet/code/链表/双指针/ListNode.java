package com.ayden.leet.code.链表.双指针;

/**
 * ListNode
 *
 * @author: xwq
 * @Date: 2023-12-10
 * @description:
 **/
public class ListNode {
    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }

}
